package com.yimo.active;

public class Active {
    public static void main(String[] args) {
        System.out.println("1&1=" + (1&1) + "; 1&0=" + (1&0) + "; 0&0=" + (0&0) + ";");
        System.out.println("1|1=" + (1|1) + "; 1|0=" + (1|0) + "; 0|0=" + (0|0) + ";");
        System.out.println("1^1=" + (1^1) + "; 1^0=" + (1^0) + "; 0^0=" + (0^0) + ";");
    }
}
